SELECT DISTINCT
	Билеты.Дата_вылета,
	Билеты.Рейс,
	Рейсы.Время_отправления
	

FROM
	Билеты
		INNER JOIN Рейсы ON Билеты.Рейс = Рейсы.Рейс
		INNER JOIN Самолеты ON Рейсы.Код_самолета = Самолеты.Код_самолета
		INNER JOIN AirbusA320 ON Самолеты.Тип_самолета = AirbusA320.Тип_самолета
		INNER JOIN Boeing787 ON Самолеты.Тип_самолета = Boeing787.Тип_самолета

	


HAVING
	FROM 
		

ORDER BY
	Билеты.Дата_вылета, Рейсы.Время_отправления